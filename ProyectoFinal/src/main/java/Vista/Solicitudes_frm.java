    private void btnGuardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat standar = new SimpleDateFormat("dd-MM-yyyy");
        String fecha = standar.format(calFechaSolicitud.getDate());
        if (this.txtNumeroSolicitud.getText().isEmpty() == false
                || this.txtCedula.getText().isEmpty() == false
                || this.txtNombre.getText().isEmpty() == false
                || this.txtCantidadDias.getText().isEmpty() == false
                || this.txtCantMinimaPasajeros.getText().isEmpty() == false
                || this.txtMarca.getText().isEmpty() == false
                || this.txtModelo.getText().isEmpty() == false
                || this.txtannio.getText().isEmpty() == false) {

            Solicitudes solicitud = new Solicitudes(
                    this.txtNumeroSolicitud.getText(),
                    cbxEstadoSolicitud.getSelectedItem().toString(),
                    fecha,
                    Integer.parseInt(this.txtCedula.getText()),
                    this.txtNombre.getText(),
                    cbxCategoria.getSelectedItem().toString(),
                    
                    Integer.parseInt(this.txtCantidadDias.getText()),
                    Integer.parseInt(this.txtCantMinimaPasajeros.getText()),
                    this.txtMarca.getText(),
                    this.txtModelo.getText(),
                    this.txtannio.getText(),
                    cbxArranqueSinLLave.isSelected(),
                    cbxCargadorInalambrico.isSelected(),
                    cbxNavegadorTraffico.isSelected(),
                    cbxSensores.isSelected(),
                    cbxCamaraTrasera.isSelected(),
                    cbxWifi.isSelected(),
                    cbxMonitoreoSatelital.isSelected()
            );
            pila_solicitudes.push(solicitud);
            resetearTablaSolicitudes();
            pila_solicitudes.estado();     
        }
        limpiar_cajas();
    }//GEN-LAST:event_btnGuardActionPerformed

