    public void llenarTablaSolicitudes() {
        String auxArranque, auxCargador, auxNavegador, auxSensor, auxCamara,
                auxWifi, auxMonitor;

        for (NodoSolicitud nodo : PilaSolicitudes.listaSolicitudes()) {
            if (nodo.getSolicitud().isArranqueSinLLave()) {
                auxArranque = "X";
            } else {
                auxArranque = "";
            }
            if (nodo.getSolicitud().isCargadorInalambrico()) {
                auxCargador = "X";
            } else {
                auxCargador = "";
            }
            if (nodo.getSolicitud().isNavegadorTraffico()) {
                auxNavegador = "X";
            } else {
                auxNavegador = "";
            }
            if (nodo.getSolicitud().isSensores()) {
                auxSensor = "X";
            } else {
                auxSensor = "";
            }
            if (nodo.getSolicitud().isCamaraTrasera()) {
                auxCamara = "X";
            } else {
                auxCamara = "";
            }
            if (nodo.getSolicitud().isWifi()) {
                auxWifi = "X";
            } else {
                auxWifi = "";
            }
            if (nodo.getSolicitud().isMonitoreoSatelital()) {
                auxMonitor = "X";
            } else {
                auxMonitor = "";
            }

            modeloTabla.addRow(new Object[]{
                nodo.getSolicitud().getIdSolicitud(),
                nodo.getSolicitud().getEstadoSolicitud(),
                nodo.getSolicitud().getFechaSolicitud(),
                nodo.getSolicitud().getCedula(),
                nodo.getSolicitud().getNombre(),
                nodo.getSolicitud().getCategoria(),
                nodo.getSolicitud().getCantidadDias(),
                nodo.getSolicitud().getCantidadMinimaPasajeros(),
                nodo.getSolicitud().getMarca(),
                nodo.getSolicitud().getModelo(),
                nodo.getSolicitud().getAnnio(),
                auxArranque,
                auxCargador,
                auxNavegador,
                auxSensor,
                auxCamara,
                auxWifi,
                auxMonitor});
        }
    }

